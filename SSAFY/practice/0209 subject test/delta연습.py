# 1. 입력 및 초기화
N = int(input())
arr = [list(map(int, input().split())) for _ in range(N)]
max_v = 0 # 또는 아주 작은 수

# 2. 모든 칸(i, j)을 주인공으로 순회
for i in range(N):
    for j in range(N):
        # [중요] 주인공 칸의 값을 초기 합으로 설정
        s = arr[i][j] 
        
        # 3. 델타 방향 설정 (상하좌우)
        for di, dj in [[-1, 0], [1, 0], [0, -1], [0, 1]]:
            # 4. 거리(c)만큼 확장 (사거리 k)
            for c in range(1, k + 1):
                ni, nj = i + di * c, j + dj * c  # 멀리 던지기
                
                # 5. 벽 체크 (경계 검사)
                if 0 <= ni < N and 0 <= nj < N:
                    s += arr[ni][nj] # 옆 칸 점수 뺏기
                else:
                    break # [꿀팁] 벽 만나면 그 방향은 더 볼 필요 없음

        # 6. 역대급 합계(max_v) 갱신
        if max_v < s:
            max_v = s

print(max_v)

