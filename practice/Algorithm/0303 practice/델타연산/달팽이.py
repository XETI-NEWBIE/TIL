T = int(input())

for tc in range(1, T + 1):
    N = int(input())
    # 1. 빈 달팽이 판 만들기
    snail = [[0] * N for _ in range(N)]
    
    # 2. 방향 설정 (우 -> 하 -> 좌 -> 상 순서)
    di = [0, 1, 0, -1]
    dj = [1, 0, -1, 0]
    
    # 시작 위치와 초기 방향(우측)
    i, j = 0, 0
    dr = 0 # di[0], dj[0]이 '우' 방향
    
    # 3. 1부터 N*N까지 숫자 채우기
    for num in range(1, N * N + 1):
        snail[i][j] = num
        
        # 현재 위치 (ni,nj)에 내가 정항방향의 이동값(dr)을 더한다
        # [dr] = dr번째 라는 뜻
        # 만약 di = [0, 1, 0, -1]이고 dr = 1이라면?
        # di[1]은 di 리스트의 1번째 값(파이썬은 0부터 세니까 두 번째 값)인 **1**을 가져오라는 명령이야.
        # i + di[dr]: 결국 "현재 세로 위치(i)에 아래쪽 방향 이동값(1)을 더해라"가 되는 거지.
        # 일단 가던 방향(dr)로 직진해보자
        ni, nj = i + di[dr], j + dj[dr]
        
        # 미로 밖으로 범위를 벗어나거나 이미 숫자가 채워져 있다면? 방향 전환!
        # if ni < 0 or ni >= N or nj < 0 or nj >= N or snail[ni][nj] != 0:
        # 교재가 그냥 if인 이유 : 범위안이라면 숫자를 더해라
        # 계속 직진하다 벽을 만나는 특수한 순간에만 방향을 바꿔야 하므로 if not을 사용
        # 범위 안이 아니거나, 이미 누가 있다면? => 문제가 생겼을 때만 꺾으라는 의미
        # snail[ni][nj] => 내가 가려고 하는 다음 칸이 비었는지 확인
        if not (0<=ni<N and 0<=nj<N) or snail[ni][nj]!=0:
            dr = (dr + 1) % 4 # 0->1->2->3->0 순으로 회전
            ni, nj = i + di[dr], j + dj[dr] # 바뀐 방향으로 다시 계산
            
        # 현재 위치 이동
        i, j = ni, nj

    # 4. 결과 출력
    print(f"#{tc}")
    for row in snail:
        print(*row)

