    n=int(input())
    snail=[[0]*n for _ in range(n)]
    
    di=[0,1,0,-1]
    dj=[1,0,-1,0]

    i,j=0
    dr=0

    for num in range(1,n*n+1):
        snail[i][j] = num
        
        ni, nj = i+di[dr], j+dj[dr]

        if ni<0 or ni>=n or nj<0 or nj>=n or snail[ni][nj]==1:
            dr=(dr+1)%4
            ni,nj = i+di[dr], j+dj[dr]

