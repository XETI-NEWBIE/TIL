# 0 <= ni < n and 0 <= nj < n: "여기는 우리 운동장(판) 안이야! 안전해!" (Safe Zone)

# not ( ... ): "어? 방금 말한 안전 구역이 아니네? **담장 밖(벽)**이네?"

# 즉, if not (안전구역) or 이미_숫자가_있음: 이 조건은
# **"앞이 벼랑 끝이거나, 이미 누가 자리를 차지했으면... 방향 꺾어!!"**라는 긴급 명령이야.



