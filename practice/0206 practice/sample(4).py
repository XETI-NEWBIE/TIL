N = map(int, input().split())

while N!=0:
    print(N)
    if N==0:
        break


# 주머니(map object)의 내용물: "나중에 누가 나를 열어보면, 안에 있는 글자들을 int로 하나씩 바꿔줘야지~"라는 작업 계획만 들어있어요.
# 그래서 채원님이 while N != 0:이라고 물어보면 컴퓨터는 이렇게 생각합니다.
# "어? N은 **'작업 지시서가 든 주머니'**인데? 숫자 0이랑은 아예 종류가 다르지! 그러니까 당연히 0이랑은 같지 않아(True)!"
# 결국 주머니 내용물이 뭐가 들어있든 상관없이, '주머니'라는 사실 그 자체 때문에 조건문이 항상 참(True)이 되어 무한 루프에 빠지는 거예요.

# map의 존재 이유는 **"리스트 안에 있는 글자 여러 개를 한꺼번에 숫자로 바꾸기 위해서"**입니다.
# **숫자 하나(6)**만 들어올 때: int() 하나면 충분합니다. 굳이 map이라는 주머니에 담을 이유가 전혀 없죠.
# **숫자 여러 개(6 10 20)**가 들어올 때: 이때는 공백으로 쪼갠(split) 리스트가 생기니까, 하나씩 숫자로 바꿔주는 map 일꾼이 필요해지는 거예요

