# 좌우로 반복하며 출력

# [문제 요약]
# 정수 N이 주어지면, N개의 줄에 걸쳐 숫자를 좌우로 번갈아가며 출력
# - 홀수 번째 줄: 1부터 N까지 순서대로 출력
# - 짝수 번째 줄: N부터 1까지 역순으로 출력

# [풀이]
# - 줄 번호(i)를 0부터 시작한다고 할 때, i % 2 == 0이면 정방향, 아니면 역방향

n = int(input())

for i in range(n):
    if i % 2 == 0:
        # 정방향 출력: 1, 2, ..., n
        for j in range(1, n + 1):
            print(j, end="")
    else:
        # 역방향 출력: n, n-1, ..., 1
        for j in range(n, 0, -1):
            print(j, end="")
    print() # 줄바꿈

