# 상하로 반복하며 출력

# [문제 요약]
# N x N 격자에 1부터 N까지의 숫자를 '상하' 방향으로 지그재그 채워 출력
# - 홀수 번째 열: 위에서 아래로 (1 -> N)
# - 짝수 번째 열: 아래에서 위로 (N -> 1)

# 1. 먼저 N x N 크기의 빈 2차원 배열을 만들기
# 2. 열(j)을 기준으로 루프를 돌며, j가 짝수면 위->아래, 홀수면 아래->위로 숫자를 채우기
# 3. 채워진 배열을 행(i) 단위로 출력

n = int(input())
grid = [[0] * n for _ in range(n)]

for j in range(n):
    if j % 2 == 0:
        # 짝수 번째 열(인덱스 0, 2..): 위에서 아래로 채우기
        num = 1
        for i in range(n):
            grid[i][j] = num
            num += 1
    else:
        # 홀수 번째 열(인덱스 1, 3..): 아래에서 위로 채우기
        num = 1
        for i in range(n - 1, -1, -1):
            grid[i][j] = num
            num += 1

# 최종 격자 출력
for row in grid:
    for val in row:
        print(val, end="")
    print()


