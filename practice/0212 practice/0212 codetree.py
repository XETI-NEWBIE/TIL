# 두 개의 격자 비교하기

# [문제]
# N x M 크기의 격자 두 개가 주어질 때, 같은 위치의 값이 같으면 0, 다르면 1을 출력

# [풀이 ]
# 1. N x M 크기의 격자 2개를 각각 입력받습니다.
# 2. 2중 for문을 돌며 같은 (i, j) 위치의 값을 비교하여 새로운 결과를 출력

import sys

# 행(n), 열(m) 입력 받기
n, m = map(int, sys.stdin.readline().split())

# 첫 번째 격자 입력
grid1 = [list(map(int, sys.stdin.readline().split())) for _ in range(n)]

# 두 번째 격자 입력
grid2 = [list(map(int, sys.stdin.readline().split())) for _ in range(n)]

# 비교 결과 출력
for i in range(n):
    for j in range(m):
        if grid1[i][j] == grid2[i][j]:
            print(0, end=" ")
        else:
            print(1, end=" ")
    print()

