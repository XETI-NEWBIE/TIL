# 특정 원소들의 합

# [문제 요약]
# 4x4 크기의 격자에 정수가 주어질 때, 특정 영역(왼쪽 아래 삼각형 형태)에 
# 해당하는 정수들의 합을 구하는 프로그램을 작성

# [풀이]
# 1. 4x4 격자를 2차원 배열로 입력
# 2. 색칠된 영역의 규칙을 파악
#    - 첫 번째 줄(i=0): 1번째 칸(j=0)까지
#    - 두 번째 줄(i=1): 2번째 칸(j=1)까지
#    - 즉, 열의 인덱스(j)가 행의 인덱스(i)보다 작거나 같은(j <= i) 칸들임

import sys

# 4x4 격자 입력 받기
grid = [list(map(int, sys.stdin.readline().split())) for _ in range(4)]

total_sum = 0

# 격자를 순회하며 조건에 맞는 원소만 더하기
for i in range(4): # 행 반복
    for j in range(4): # 열 반복
        # 색칠된 칸의 조건: 열 인덱스가 행 인덱스 이하인 경우
        if j <= i:
            total_sum += grid[i][j]

print(total_sum)

